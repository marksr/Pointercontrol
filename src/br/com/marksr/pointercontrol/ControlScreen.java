    public interface MouseEventInterface {
        void mouseMove(Point point);
        void mouseClick(Point point);
        void mouseDrag(Point point);
        void mouseDown(Point point);
        void mouseUp(Point point);
    }

    public class MouseEventPack {
        public static final int MOUSE_MOVE = 0;
        public static final int MOUSE_CLICK = 1;
        public static final int MOUSE_DRAG = 2;
        public static final int MOUSE_UP = 3;
        public static final int MOUSE_DOWN = 4;
        public final int type;
        public final Point point;

        public MouseEventPack(int type, Point point) {
            this.type = type;
            this.point = point;
        }
    }
