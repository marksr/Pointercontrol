    public ControlScreen() {
        initComponents();
        
        mouseListeners = new GenericPublisher<MouseEventInterface, MouseEventPack>() {

            @Override
            public void action(MouseEventInterface l, MouseEventPack d) {
                switch (d.type) {
                    case MouseEventPack.MOUSE_MOVE:
                        l.mouseMove(d.point);
                        break;
                    case MouseEventPack.MOUSE_CLICK:
                        l.mouseClick(d.point);
                        break;
                    case MouseEventPack.MOUSE_DRAG:
                        l.mouseDrag(d.point);
                        break;
                    case MouseEventPack.MOUSE_DOWN:
                        l.mouseDown(d.point);
                        break;
                    case MouseEventPack.MOUSE_UP:
                        l.mouseUp(d.point);
                        break;
                }
            }
        };
        
        closingEventListeners = new GenericPublisher<ClosingEventInterface, Void>() {

            @Override
            public void action(ClosingEventInterface l, Void d) {
                l.closing();
            }
        };
    }
    
    public void setOnMouseMoveEvent(MouseEventInterface event) {
        mouseListeners.setOnEvent(event);
    }

    public void removeOnMouseMoveEvent(MouseEventInterface event) {
        mouseListeners.removeOnEvent(event);
    }
    
    public void setOnClosingEvent(ClosingEventInterface event) {
        closingEventListeners.setOnEvent(event);
    }

    public void removeOnClosingEvent(ClosingEventInterface event) {
        closingEventListeners.removeOnEvent(event);
    }    
    
